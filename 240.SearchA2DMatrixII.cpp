/*
 * 240. Search a 2D Matrix II
 * https://leetcode.com/problems/search-a-2d-matrix-ii/
 *
 * 编写一个高效的算法来搜索 m x n 矩阵 matrix 中的一个目标值 target。
 * 该矩阵具有以下特性：
 *   - 每行的元素从左到右升序排列
 *   - 每列的元素从上到下升序排列
 *
 * ──────────────────────────────────────────────
 * 解法一：暴力遍历（Brute Force）
 * ──────────────────────────────────────────────
 * 思路：
 *   双重循环逐个检查矩阵中的每一个元素，判断是否等于 target。
 *
 * 图解：matrix =                  target = 5
 *     [1,  4,  7, 11, 15]
 *     [2,  5,  8, 12, 19]
 *     [3,  6,  9, 16, 22]
 *     [10, 13, 14, 17, 24]
 *     [18, 21, 23, 26, 30]
 *
 *   逐个扫描：1→4→7→11→15→2→5 找到！ ✅
 *
 * 时间复杂度：O(m * n)  —— 最坏遍历全部元素
 * 空间复杂度：O(1)      —— 只用常数变量
 */
class Solution {
public:
    bool searchMatrix(vector<vector<int>>& matrix, int target) {
        int m = (int)matrix.size();
        int n = (int)matrix[0].size();

        for (int i = 0; i < m; ++i)
        {
            for (int j = 0; j < n; ++j)
            {
                if (matrix[i][j] == target)
                {
                    return true;
                }
            }
        }

        return false;
    }
};

/*
 * ──────────────────────────────────────────────
 * 解法二：逐行二分查找（Binary Search per Row）
 * ──────────────────────────────────────────────
 * 思路：
 *   每行已升序排列，对每一行做一次二分查找。
 *   如果某一行找到 target 就返回 true，全部行都没找到则返回 false。
 *
 * 图解：target = 5
 *     [1,  4,  7, 11, 15]  ← 二分：mid=7 > 5 → 左半 → mid=4 < 5 → 右半 → mid=7 > 5 → 未找到
 *     [2,  5,  8, 12, 19]  ← 二分：mid=8 > 5 → 左半 → mid=5 == 5 → 找到！ ✅
 *     ...
 *
 * 时间复杂度：O(m * logn)  —— m 行，每行二分 O(logn)
 * 空间复杂度：O(1)         —— 只用常数变量
 */
class Solution2 {
public:
    bool searchMatrix(vector<vector<int>>& matrix, int target) {
        int m = (int)matrix.size();
        int n = (int)matrix[0].size();

        for (int i = 0; i < m; ++i)
        {
            // 剪枝：当前行最小值 > target，后续行更大，不可能找到
            if (matrix[i][0] > target)
            {
                break;
            }
            // 剪枝：当前行最大值 < target，跳过这一行
            if (matrix[i][n - 1] < target)
            {
                continue;
            }

            // 对当前行二分查找
            int lo = 0, hi = n - 1;
            while (lo <= hi)
            {
                int mid = lo + (hi - lo) / 2;
                if (matrix[i][mid] == target)
                {
                    return true;
                }
                else if (matrix[i][mid] < target)
                {
                    lo = mid + 1;
                }
                else
                {
                    hi = mid - 1;
                }
            }
        }

        return false;
    }
};

/*
 * ──────────────────────────────────────────────
 * 解法三：右上角搜索 / Z 字形查找（Search from Top-Right）
 * ──────────────────────────────────────────────
 *
 * 一、核心思想
 *
 *   从矩阵的右上角 (0, n-1) 出发，利用排列性质进行搜索：
 *     - 当前值 == target → 找到
 *     - 当前值 > target  → 当前列所有元素都 > target（列递增），排除整列，左移
 *     - 当前值 < target  → 当前行所有元素都 < target（已扫过的右侧更大也不够），排除整行，下移
 *
 *   每一步要么排除一整行，要么排除一整列，所以最多走 m + n 步。
 *
 *   为什么选右上角（或左下角）？
 *     右上角：往左变小，往下变大 —— 两个方向"单调性相反"，可以明确决策方向。
 *     左上角：往右变大，往下也变大 —— 两个方向同向，无法决策。
 *     右下角：往左变小，往上也变小 —— 两个方向同向，无法决策。
 *     左下角：往右变大，往上变小 —— 与右上角对称，同样可行。
 *
 * 二、图解
 *
 *   matrix =                      target = 5
 *     [ 1,  4,  7, 11, 15]
 *     [ 2,  5,  8, 12, 19]
 *     [ 3,  6,  9, 16, 22]
 *     [10, 13, 14, 17, 24]
 *     [18, 21, 23, 26, 30]
 *
 *   从右上角 (0, 4) = 15 出发：
 *
 *     row=0, col=4: 15 > 5  → 排除第 4 列，col--
 *
 *       [ 1,  4,  7,[11],  ·]
 *       [ 2,  5,  8, 12,   ·]      15 比 5 大，
 *       [ 3,  6,  9, 16,   ·]      它下面的 19,22,24,30 更大，
 *       [10, 13, 14, 17,   ·]      整列排除 ←
 *       [18, 21, 23, 26,   ·]
 *
 *     row=0, col=3: 11 > 5  → 排除第 3 列，col--
 *
 *       [ 1,  4, [7],  ·,  ·]
 *       [ 2,  5,  8,   ·,  ·]      同理排除 ←
 *       [ 3,  6,  9,   ·,  ·]
 *       [10, 13, 14,   ·,  ·]
 *       [18, 21, 23,   ·,  ·]
 *
 *     row=0, col=2: 7 > 5   → 排除第 2 列，col--
 *
 *       [ 1, [4],  ·,  ·,  ·]
 *       [ 2,  5,   ·,  ·,  ·]      同理排除 ←
 *       [ 3,  6,   ·,  ·,  ·]
 *       [10, 13,   ·,  ·,  ·]
 *       [18, 21,   ·,  ·,  ·]
 *
 *     row=0, col=1: 4 < 5   → 排除第 0 行，row++
 *
 *       [ ·,  ·,   ·,  ·,  ·]      4 比 5 小，
 *       [ 2, [5],  ·,  ·,  ·]      它左边的 1 更小，
 *       [ 3,  6,   ·,  ·,  ·]      整行排除 ↓
 *       [10, 13,   ·,  ·,  ·]
 *       [18, 21,   ·,  ·,  ·]
 *
 *     row=1, col=1: 5 == 5  → 找到！ ✅
 *
 *   搜索路径示意：
 *     [ ·,  ·,  ·,  ·, 15]
 *     [ ·,  5←, ·, ←11,↑ ]
 *     [ ·,  ·,  ·, ←7, · ]
 *     [ ·,  ·,  ·,  ·, · ]     共走 5 步
 *     [ ·,  ·,  ·,  ·, · ]
 *                ↑
 *         实际路径：15 → 11 → 7 → 4 → 5
 *
 * 三、对比
 *
 *   - 解法一 O(m*n) 暴力，完全没利用有序性
 *   - 解法二 O(m*logn) 逐行二分，利用了行内有序性
 *   - 解法三 O(m+n) 右上角搜索，同时利用行和列的有序性，是最优解
 *
 * 时间复杂度：O(m + n)  —— 每步排除一行或一列，最多走 m + n 步
 * 空间复杂度：O(1)      —— 只用两个变量
 */
class Solution3 {
public:
    bool searchMatrix(vector<vector<int>>& matrix, int target) {
        int m = (int)matrix.size();
        int n = (int)matrix[0].size();

        // 从右上角出发
        int row = 0;
        int col = n - 1;

        while (row < m && col >= 0)
        {
            if (matrix[row][col] == target)
            {
                return true;
            }
            else if (matrix[row][col] > target)
            {
                col--; // 当前值太大，排除此列，左移
            }
            else
            {
                row++; // 当前值太小，排除此行，下移
            }
        }

        return false;
    }
};
