/*
 * 189. Rotate Array
 * https://leetcode.com/problems/rotate-array/
 *
 * 给定一个整数数组 nums，将数组中的元素向右轮转 k 个位置，其中 k 是非负数。
 * 要求原地修改数组。
 *
 * 示例：
 *   输入：nums = [1,2,3,4,5,6,7], k = 3
 *   输出：[5,6,7,1,2,3,4]
 *
 *   向右轮转 1 步：[7,1,2,3,4,5,6]
 *   向右轮转 2 步：[6,7,1,2,3,4,5]
 *   向右轮转 3 步：[5,6,7,1,2,3,4]
 *
 * ──────────────────────────────────────────────
 * 解法一：暴力旋转（Brute Force）
 * ──────────────────────────────────────────────
 * 思路：
 *   每次将数组所有元素向右移动一步，重复 k 次。
 *   向右移动一步：先保存末尾元素，然后从后往前依次后移，最后把末尾元素放到首位。
 *
 * 图解：nums = [1,2,3,4,5,6,7], k = 3
 *
 *   第 1 轮（右移 1 步）：
 *     保存末尾 tmp=7
 *     后移：[_,1,2,3,4,5,6]
 *     首位填 tmp：[7,1,2,3,4,5,6]
 *
 *   第 2 轮（右移 1 步）：
 *     保存末尾 tmp=6
 *     后移：[_,7,1,2,3,4,5]
 *     首位填 tmp：[6,7,1,2,3,4,5]
 *
 *   第 3 轮（右移 1 步）：
 *     保存末尾 tmp=5
 *     后移：[_,6,7,1,2,3,4]
 *     首位填 tmp：[5,6,7,1,2,3,4] ✅
 *
 * 时间复杂度：O(n * k)  —— 每轮移动 n 个元素，共 k 轮
 * 空间复杂度：O(1)      —— 只用一个临时变量
 */
class Solution {
public:
    void rotate(vector<int>& nums, int k) {
        int n = nums.size();
        k %= n; // k 可能大于 n，取模消除多余整圈

        for (int step = 0; step < k; ++step)
        {
            int tmp = nums[n - 1]; // 保存末尾元素
            // 从后往前，每个元素右移一位
            for (int i = n - 1; i > 0; --i)
            {
                nums[i] = nums[i - 1];
            }
            nums[0] = tmp; // 末尾元素放到首位
        }
    }
};

/*
 * ──────────────────────────────────────────────
 * 解法二：额外数组（Extra Array）
 * ──────────────────────────────────────────────
 * 思路：
 *   每个元素向右移动 k 位后的新下标为 (i + k) % n。
 *   用一个额外数组存放旋转后的结果，再拷贝回原数组。
 *
 * 图解：nums = [1,2,3,4,5,6,7], k = 3, n = 7
 *
 *   原下标 i → 新下标 (i+3) % 7
 *     i=0: 1 → 位置 3
 *     i=1: 2 → 位置 4
 *     i=2: 3 → 位置 5
 *     i=3: 4 → 位置 6
 *     i=4: 5 → 位置 0
 *     i=5: 6 → 位置 1
 *     i=6: 7 → 位置 2
 *
 *   tmp = [5,6,7,1,2,3,4] ✅
 *   拷贝回 nums
 *
 * 时间复杂度：O(n)  —— 遍历一次 + 拷贝一次
 * 空间复杂度：O(n)  —— 需要额外数组
 */
class Solution2 {
public:
    void rotate(vector<int>& nums, int k) {
        int n = nums.size();
        k %= n;

        vector<int> tmp(n);
        for (int i = 0; i < n; ++i)
        {
            tmp[(i + k) % n] = nums[i];
        }

        nums = tmp; // 拷贝回原数组
    }
};

/*
 * ──────────────────────────────────────────────
 * 解法三：三次翻转（Three Reverses）
 * ──────────────────────────────────────────────
 *
 * 一、核心思想
 *
 *   向右轮转 k 位，等价于：
 *     1. 先翻转整个数组
 *     2. 再翻转前 k 个元素
 *     3. 最后翻转后 n-k 个元素
 *
 *   为什么这样做是对的？
 *     原数组可以看作两段：[A | B]，其中 A = nums[0..n-k-1]，B = nums[n-k..n-1]
 *     目标是得到 [B | A]（B 移到前面）。
 *
 *     第 1 步：翻转整体 → [A|B] 的逆 = [B逆 | A逆]
 *     第 2 步：翻转前 k 个（即 B逆）→ [B | A逆]
 *     第 3 步：翻转后 n-k 个（即 A逆）→ [B | A]  ✅
 *
 * 二、图解
 *
 *   nums = [1,2,3,4,5,6,7], k = 3, n = 7
 *
 *   将数组分为两段：A = [1,2,3,4]  B = [5,6,7]
 *   目标：[B|A] = [5,6,7,1,2,3,4]
 *
 *   第 1 步：翻转整个数组
 *     [1,2,3,4,5,6,7] → [7,6,5,4,3,2,1]
 *      ─────────────────────────────────
 *               reverse(0, 6)
 *
 *   第 2 步：翻转前 k=3 个元素
 *     [7,6,5,4,3,2,1] → [5,6,7,4,3,2,1]
 *      ───────
 *      reverse(0, 2)
 *
 *   第 3 步：翻转后 n-k=4 个元素
 *     [5,6,7,4,3,2,1] → [5,6,7,1,2,3,4]
 *              ───────────
 *              reverse(3, 6)
 *
 *   结果：[5,6,7,1,2,3,4] ✅
 *
 * 三、对比
 *
 *   - 解法一 O(n*k) 在 k 很大时太慢
 *   - 解法二 O(n) 时间但需要 O(n) 额外空间
 *   - 解法三 O(n) 时间 + O(1) 空间，是最优解
 *
 * 时间复杂度：O(n)  —— 三次翻转各 O(n)，总共 O(n)
 * 空间复杂度：O(1)  —— 原地操作，只用常数额外空间
 */
class Solution3 {
public:
    void rotate(vector<int>& nums, int k) {
        int n = nums.size();
        k %= n; // k 可能大于 n，取模消除多余整圈

        if (k == 0) return; // 无需旋转

        // 第 1 步：翻转整个数组
        reverse(nums.begin(), nums.end());
        // 第 2 步：翻转前 k 个元素
        reverse(nums.begin(), nums.begin() + k);
        // 第 3 步：翻转后 n-k 个元素
        reverse(nums.begin() + k, nums.end());
    }
};
