/*
 * 142. Linked List Cycle II
 * https://leetcode.com/problems/linked-list-cycle-ii/
 *
 * 给定一个链表的头节点 head，返回链表开始入环的第一个节点。
 * 如果链表无环，则返回 null。不允许修改链表。
 *
 *   链表结构：
 *     struct ListNode {
 *         int val;
 *         ListNode *next;
 *         ListNode(int x) : val(x), next(NULL) {}
 *     };
 *
 * ──────────────────────────────────────────────
 * 解法一：哈希集合（Hash Set）
 * ──────────────────────────────────────────────
 *
 * 一、核心思想
 *
 *   遍历链表，将每个访问过的节点存入 HashSet。
 *   第一个重复出现的节点就是环的入口。
 *   如果遍历到 null，说明无环。
 *
 * 二、图解
 *
 *   链表：3 → 2 → 0 → -4
 *              ↑           |
 *              └───────────┘     pos = 1（环入口是节点 2）
 *
 *   遍历过程：
 *     访问 3：set = {3}，继续
 *     访问 2：set = {3, 2}，继续
 *     访问 0：set = {3, 2, 0}，继续
 *     访问-4：set = {3, 2, 0, -4}，继续
 *     访问 2：2 ∈ set → 环入口是节点 2！ ✅
 *
 * 时间复杂度：O(n)  —— 最多遍历所有节点一次
 * 空间复杂度：O(n)  —— 需要存储所有已访问节点
 */
class Solution {
public:
    ListNode *detectCycle(ListNode *head) {
        unordered_set<ListNode*> visited;

        ListNode *cur = head;
        while (cur != nullptr)
        {
            // 第一个重复出现的节点就是环入口
            if (visited.count(cur))
            {
                return cur;
            }
            visited.insert(cur);
            cur = cur->next;
        }

        // 遍历到 null，无环
        return nullptr;
    }
};

/*
 * ──────────────────────────────────────────────
 * 解法二：快慢指针（Floyd 判圈算法 — 完整版）
 * ──────────────────────────────────────────────
 *
 * 一、核心思想
 *
 *   分两个阶段：
 *     阶段 1：快慢指针判断是否有环（与 141 题相同）
 *     阶段 2：找到环的入口节点（数学推导）
 *
 *   阶段 2 的关键结论：
 *     快慢指针相遇后，让一个指针回到 head，另一个留在相遇点，
 *     两个指针都以步长 1 前进，再次相遇的地方就是环的入口。
 *
 * 二、数学推导（为什么这样做是对的）
 *
 *   设：
 *     a = 链表起点到环入口的距离
 *     b = 环入口到相遇点的距离
 *     c = 环的长度
 *
 *     起点 ──a──→ 环入口 ──b──→ 相遇点
 *                    ↑                |
 *                    └──── c - b ─────┘
 *
 *   相遇时：
 *     slow 走了：a + b 步
 *     fast 走了：a + b + nc 步（在环里多绕了 n 圈）
 *
 *   由于 fast 速度是 slow 的 2 倍：
 *     2(a + b) = a + b + nc
 *     a + b = nc
 *     a = nc - b = (n-1)c + (c - b)
 *
 *   关键等式：a = (n-1)c + (c - b)
 *
 *   含义：
 *     从起点走 a 步 → 到达环入口
 *     从相遇点走 (c-b) 步 → 到达环入口（再绕 n-1 圈还是环入口）
 *
 *   所以：一个指针从起点出发，一个从相遇点出发，
 *         都走 1 步，走 a 步后一定在环入口相遇！
 *
 * 三、图解
 *
 *   链表：3 → 2 → 0 → -4 → (回到 2)
 *         a=1    环入口     b=2（环入口到相遇点）
 *                           c=3（环长）
 *
 *   阶段 1：快慢指针找相遇点
 *
 *     步骤   slow      fast       说明
 *     ────   ────      ────       ────
 *      0     3(idx0)   3(idx0)    起点
 *      1     2(idx1)   0(idx2)    slow+1, fast+2
 *      2     0(idx2)  -4(idx3)
 *      3    -4(idx3)   0(idx2)    fast 绕回
 *      4     2(idx1)  -4(idx3)
 *      5     0(idx2)   0(idx2)    相遇！ ← 在节点 0 处
 *
 *   阶段 2：找环入口
 *
 *     让 p1 回到 head（节点 3），p2 留在相遇点（节点 0）
 *     两者都以步长 1 前进：
 *
 *     步骤   p1        p2         说明
 *     ────   ──        ──         ────
 *      0     3(idx0)   0(idx2)    起始
 *      1     2(idx1)  -4(idx3)    各走 1 步
 *      2     —         2(idx1)    p1 也到了节点 2 → 相遇！环入口 = 节点 2 ✅
 *
 *   验证：a = 1，c - b = 3 - 2 = 1，a == c - b ✓
 *
 * 四、完整流程总结
 *
 *     ┌─────────────────────────────────────────┐
 *     │  1. slow 走 1 步，fast 走 2 步           │
 *     │  2. 如果 fast 到 null → 无环，返回 null  │
 *     │  3. 如果 slow == fast → 有环，进入阶段 2 │
 *     │  4. p1 = head，p2 = 相遇点              │
 *     │  5. 两者各走 1 步，相遇处即为环入口      │
 *     └─────────────────────────────────────────┘
 *
 * 时间复杂度：O(n)  —— 阶段 1 最多 O(n)，阶段 2 最多 O(n)
 * 空间复杂度：O(1)  —— 只用几个指针
 */
class Solution2 {
public:
    ListNode *detectCycle(ListNode *head) {
        // 阶段 1：快慢指针判断是否有环
        ListNode *slow = head;
        ListNode *fast = head;

        while (fast != nullptr && fast->next != nullptr)
        {
            slow = slow->next;
            fast = fast->next->next;

            // 快慢指针相遇，说明有环
            if (slow == fast)
            {
                // 阶段 2：找环入口
                ListNode *p1 = head;    // 从起点出发
                ListNode *p2 = slow;    // 从相遇点出发

                while (p1 != p2)
                {
                    p1 = p1->next;      // 都走 1 步
                    p2 = p2->next;
                }

                return p1; // 相遇点即为环入口
            }
        }

        // fast 到达终点，无环
        return nullptr;
    }
};

