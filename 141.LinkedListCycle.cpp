/*
 * 141. Linked List Cycle
 * https://leetcode.com/problems/linked-list-cycle/
 *
 * 给你一个链表的头节点 head，判断链表中是否有环。
 * 如果链表中有某个节点，可以通过连续跟踪 next 指针再次到达，则链表中存在环。
 * 如果链表中存在环，则返回 true；否则，返回 false。
 *
 *   链表结构：
 *     struct ListNode {
 *         int val;
 *         ListNode *next;
 *         ListNode(int x) : val(x), next(NULL) {}
 *     };
 *
 * ──────────────────────────────────────────────
 * 解法一：哈希集合（Hash Set）
 * ──────────────────────────────────────────────
 *
 * 一、核心思想
 *
 *   遍历链表，将访问过的节点地址存入 HashSet。
 *   如果当前节点已经在 Set 中出现过，说明我们"回到"了之前的节点 → 有环。
 *   如果遍历到 null，说明链表有终点 → 无环。
 *
 * 二、图解
 *
 *   有环示例：
 *     3 → 2 → 0 → -4
 *         ↑           |
 *         └───────────┘     pos = 1（尾节点连接到索引 1 的节点）
 *
 *   遍历过程：
 *     访问 3：set = {3}，继续
 *     访问 2：set = {3, 2}，继续
 *     访问 0：set = {3, 2, 0}，继续
 *     访问-4：set = {3, 2, 0, -4}，继续
 *     访问 2：2 ∈ set → 有环！ ✅
 *
 *   无环示例：
 *     1 → 2 → null
 *
 *   遍历过程：
 *     访问 1：set = {1}，继续
 *     访问 2：set = {1, 2}，继续
 *     到达 null → 无环 ✅
 *
 * 时间复杂度：O(n)  —— 最多遍历所有节点一次
 * 空间复杂度：O(n)  —— 需要存储所有已访问节点
 */
class Solution {
public:
    bool hasCycle(ListNode *head) {
        unordered_set<ListNode*> visited;

        ListNode *cur = head;
        while (cur != nullptr)
        {
            // 如果当前节点已访问过，说明有环
            if (visited.count(cur))
            {
                return true;
            }
            visited.insert(cur);
            cur = cur->next;
        }

        // 遍历到 null，说明无环
        return false;
    }
};

/*
 * ──────────────────────────────────────────────
 * 解法二：快慢指针（Floyd 判圈算法 / 龟兔赛跑）
 * ──────────────────────────────────────────────
 *
 * 一、核心思想
 *
 *   设置两个指针：
 *     slow（慢指针）：每次走 1 步
 *     fast（快指针）：每次走 2 步
 *
 *   如果链表有环，fast 一定会在环内"追上" slow（两者相遇）。
 *   如果链表无环，fast 会先到达 null。
 *
 * 二、为什么有环时一定会相遇？
 *
 *   当两个指针都进入环后，考虑它们之间的距离变化：
 *
 *   每走一步：
 *     fast 前进 2 步，slow 前进 1 步
 *     → 两者距离每次缩小 1
 *
 *   设进入环时 fast 领先 slow d 步（或者说 slow 领先环长-d 步），
 *   那么经过 d 步后，距离缩小为 0 → 相遇！
 *
 *   更直观的理解：
 *     想象操场跑圈，快的人速度是慢的人的 2 倍，
 *     快的人一定会"套圈"追上慢的人。
 *
 * 三、图解
 *
 *   有环链表：3 → 2 → 0 → -4 → (回到 2)
 *
 *   用索引表示位置：
 *     节点：  3(0)  2(1)  0(2)  -4(3)
 *     环：    1 → 2 → 3 → 1 → 2 → 3 → ...
 *
 *   模拟过程：
 *     步骤   slow位置   fast位置   说明
 *     ────   ────────   ────────   ────
 *      0     0(节点3)   0(节点3)   起点
 *      1     1(节点2)   2(节点0)   slow+1, fast+2
 *      2     2(节点0)   4→1(节点2) fast 绕回环内（4 mod 环长3 = 1）
 *      3     3(节点-4)  3(节点-4)  相遇！ ✅
 *
 *   无环链表：1 → 2 → 3 → null
 *
 *   模拟过程：
 *     步骤   slow位置   fast位置
 *     ────   ────────   ────────
 *      0     1          1
 *      1     2          3
 *      2     —          fast->next == null，停止 → 无环 ✅
 *
 * 四、循环终止条件
 *
 *   while (fast != nullptr && fast->next != nullptr)
 *
 *   为什么要检查 fast 和 fast->next？
 *   - fast == nullptr：链表节点数为偶数时，fast 恰好走到 null
 *   - fast->next == nullptr：链表节点数为奇数时，fast 走到最后一个节点
 *   两种情况都说明链表有终点 → 无环
 *
 * 时间复杂度：O(n)
 *   - 无环：fast 先到终点，最多 n/2 步
 *   - 有环：slow 进入环后，最多再走"环长"步就会被追上
 *     总体不超过 O(n)
 * 空间复杂度：O(1)  —— 只用两个指针
 */
class Solution2 {
public:
    bool hasCycle(ListNode *head) {
        ListNode *slow = head;
        ListNode *fast = head;

        while (fast != nullptr && fast->next != nullptr)
        {
            slow = slow->next;       // 慢指针走 1 步
            fast = fast->next->next; // 快指针走 2 步

            // 快慢指针相遇，说明有环
            if (slow == fast)
            {
                return true;
            }
        }

        // fast 到达终点，说明无环
        return false;
    }
};

/*
 * ──────────────────────────────────────────────
 * 解法对比
 * ──────────────────────────────────────────────
 *
 *   解法          时间复杂度    空间复杂度    特点
 *   ────          ────────      ────────      ────
 *   哈希集合      O(n)          O(n)          直观，但需要额外空间
 *   快慢指针      O(n)          O(1)          最优解，经典的 Floyd 算法
 *
 *   面试建议：
 *   - 直接写快慢指针解法，是链表题的必备技巧
 *   - 延伸：142 题（找环的入口节点）在此基础上再加一步推导
 *
 * ──────────────────────────────────────────────
 * 延伸：Floyd 算法的数学本质
 * ──────────────────────────────────────────────
 *
 *   设链表起点到环入口距离 = a，环长 = c。
 *
 *     起点 ──a──→ 环入口 ──→ ... ──→ (回到环入口)
 *                    └─────── c ────────┘
 *
 *   当 slow 走了 k 步时，fast 走了 2k 步。
 *   两者相遇意味着它们在环内的位置相同：
 *     2k - k = nc（差值是环长的整数倍）
 *     → k = nc
 *
 *   即 slow 走了 nc 步时两者相遇。
 *   这保证了在 O(n) 步内一定能相遇。
 */
